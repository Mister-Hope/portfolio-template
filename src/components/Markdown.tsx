import type { FC } from "react";
import { RichContent } from "./RichContent.js";

export interface MarkdownProps {
  /**
   * Content text (Markdown supported)
   * 内容文本 (支持 Markdown)
   */
  content: string;
  /**
   * Whether to display as a card
   * 是否显示为卡片
   */
  card?: boolean;
}

/**
 * Markdown component
 *
 * Displays a simple text block with glassmorphism styling.
 *
 * Markdown 组件
 *
 * 显示带有玻璃拟态样式的简单文本块。
 */
export const Markdown: FC<MarkdownProps> = ({ content, card = false }) => {
  if (card)
    return (
      <div className="card-base text-content p-8 font-medium md:p-10">
        <RichContent content={content} />
      </div>
    );

  return (
    <div className="text-content px-2 font-medium md:px-4">
      <RichContent content={content} />
    </div>
  );
};
